SELECT 
T.EXECUTION_ID,
T.CUSTOMER_ID,
T.CURRENCY_PAIR,
T.SIDE,
T.EXECUTION_AMOUNT,
T.EXECUTION_PRICE,
T.EXECUTION_DATE,
T.EXECUTION_DATETIME,
T.SPOT_PL,
JP.LOT_SIZE
FROM (SELECT
JE.EXECUTION_ID,
JE.PRODUCT_ID,
JE.CUSTOMER_ID,
JE.CURRENCY_PAIR,
JE.SIDE,
JE.EXECUTION_AMOUNT,
JE.EXECUTION_PRICE,
JE.EXECUTION_DATE,
JE.EXECUTION_DATETIME,
JCB.SPOT_PL
FROM JHF_ALIVE_EXECUTION JE ,JHF_ALIVE_CONTRACT_BIND JCB 
WHERE JE.EXECUTION_ID=JCB.EXECUTION_ID
AND JE.EXECUTION_DATE BETWEEN '20120613'AND  '20120614' 
AND JE.TRADE_TYPE IN (1,2) AND JE.CUSTOMER_ID='00000120'
AND JE.FORCE_RELATION_FLAG=0 
AND JE.ACTIVE_FLAG=1
AND JCB.ACTIVE_FLAG=1
UNION ALL 
SELECT
JE.EXECUTION_ID,
JE.PRODUCT_ID,
JE.CUSTOMER_ID,
JE.CURRENCY_PAIR,
JE.SIDE,
JE.EXECUTION_AMOUNT,
JE.EXECUTION_PRICE,
JE.EXECUTION_DATE,
JE.EXECUTION_DATETIME,
JCB.SPOT_PL
FROM JHF_FINISHED_EXECUTION JE ,JHF_FINISHED_CONTRACT_BIND JCB 
WHERE JE.EXECUTION_ID=JCB.EXECUTION_ID
AND JE.EXECUTION_DATE BETWEEN  '20120613'AND  '20120614'  
AND JE.TRADE_TYPE IN (1,2) AND JE.CUSTOMER_ID='00000120'
AND JE.FORCE_RELATION_FLAG=0 
AND JE.ACTIVE_FLAG=1
AND JCB.ACTIVE_FLAG=1
) T , JHF_PRODUCT JP
WHERE T.PRODUCT_ID=JP.PRODUCT_ID
ORDER BY T.EXECUTION_DATETIME ASC , T.EXECUTION_ID ASC  ;








